h2o.runif <- function(x, seed = -1) {
  if (!is(x, "H2OFrame")) stop("`data` must be an H2OFrame object")
  if (!is.numeric(seed) || length(seed) != 1L || !is.finite(seed)) stop("`seed` must be an integer >= 0")

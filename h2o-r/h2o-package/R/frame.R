  if(length(l) == 0L)
    .h2o.nary_frame_op("sapply", X, fun.ast)
  else
    .h2o.nary_frame_op("sapply", X, fun.ast, fun_args = l)  # see the developer note in ast.R for info on the special "fun_args" parameter
})

#'
